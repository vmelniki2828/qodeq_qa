import React, { useState, useEffect, useRef } from 'react';
import './Calendar.css';

export const Calendar = ({ value, onChange, placeholder = "Select date", isOpen, onOpen, onClose, minDate, maxDate }) => {
  const [currentDate, setCurrentDate] = useState(new Date());
  const [position, setPosition] = useState({ top: true, left: true });
  const calendarRef = useRef(null);
  
  const selectedDate = value ? new Date(value) : null;

  // –í—ã—á–∏—Å–ª–µ–Ω–∏–µ –ø–æ–∑–∏—Ü–∏–∏ –∫–∞–ª–µ–Ω–¥–∞—Ä—è
  const calculatePosition = () => {
    if (isOpen && calendarRef.current) {
      const rect = calendarRef.current.getBoundingClientRect();
      const viewportWidth = window.innerWidth;
      const viewportHeight = window.innerHeight;
      const isMobile = viewportWidth <= 768;
      
      const calendarWidth = isMobile ? 320 : 350;
      const calendarHeight = isMobile ? 350 : 400;
      
      const newPosition = {
        top: isMobile ? false : rect.bottom + calendarHeight <= viewportHeight,
        left: rect.left + calendarWidth <= viewportWidth
      };
      
      setPosition(newPosition);
    }
  };

  useEffect(() => {
    calculatePosition();
  }, [isOpen]);

  // –ü–µ—Ä–µ—Å—á–µ—Ç –ø–æ–∑–∏—Ü–∏–∏ –ø—Ä–∏ –∏–∑–º–µ–Ω–µ–Ω–∏–∏ —Ä–∞–∑–º–µ—Ä–∞ –æ–∫–Ω–∞
  useEffect(() => {
    const handleResize = () => {
      calculatePosition();
    };

    window.addEventListener('resize', handleResize);
    return () => {
      window.removeEventListener('resize', handleResize);
    };
  }, [isOpen]);

  // –ó–∞–∫—Ä—ã—Ç–∏–µ –∫–∞–ª–µ–Ω–¥–∞—Ä—è –ø—Ä–∏ –∫–ª–∏–∫–µ –≤–Ω–µ –µ–≥–æ
  useEffect(() => {
    const handleClickOutside = (event) => {
      if (calendarRef.current && !calendarRef.current.contains(event.target) && isOpen) {
        if (onClose) onClose();
      }
    };

    document.addEventListener('mousedown', handleClickOutside);
    return () => {
      document.removeEventListener('mousedown', handleClickOutside);
    };
  }, [isOpen, onClose]);

  const formatDate = (date) => {
    if (!date) return placeholder;
    return date.toLocaleDateString('ru-RU', {
      year: 'numeric',
      month: '2-digit',
      day: '2-digit'
    });
  };

  const getDaysInMonth = (date) => {
    return new Date(date.getFullYear(), date.getMonth() + 1, 0).getDate();
  };

  const getFirstDayOfMonth = (date) => {
    return new Date(date.getFullYear(), date.getMonth(), 1).getDay();
  };

  const isDateDisabled = (day) => {
    const date = new Date(currentDate.getFullYear(), currentDate.getMonth(), day);
    
    if (minDate && date < minDate) {
      return true;
    }
    
    if (maxDate && date > maxDate) {
      return true;
    }
    
    return false;
  };

  const handleDateSelect = (day) => {
    if (isDateDisabled(day)) {
      return; // –ù–µ –ø–æ–∑–≤–æ–ª—è–µ–º –≤—ã–±–∏—Ä–∞—Ç—å –∑–∞–±–ª–æ–∫–∏—Ä–æ–≤–∞–Ω–Ω—ã–µ –¥–∞—Ç—ã
    }
    
    const newDate = new Date(currentDate.getFullYear(), currentDate.getMonth(), day);
    // –ò—Å–ø–æ–ª—å–∑—É–µ–º –ª–æ–∫–∞–ª—å–Ω–æ–µ –≤—Ä–µ–º—è –¥–ª—è –∏–∑–±–µ–∂–∞–Ω–∏—è –ø—Ä–æ–±–ª–µ–º —Å —á–∞—Å–æ–≤—ã–º–∏ –ø–æ—è—Å–∞–º–∏
    const year = newDate.getFullYear();
    const month = String(newDate.getMonth() + 1).padStart(2, '0');
    const dayStr = String(newDate.getDate()).padStart(2, '0');
    const formattedDate = `${year}-${month}-${dayStr}`;
    
    onChange(formattedDate);
    if (onClose) onClose();
  };

  const handlePrevMonth = () => {
    const newDate = new Date(currentDate.getFullYear(), currentDate.getMonth() - 1);
    
    // –ü—Ä–æ–≤–µ—Ä—è–µ–º, –º–æ–∂–Ω–æ –ª–∏ –ø–µ—Ä–µ–π—Ç–∏ –∫ –ø—Ä–µ–¥—ã–¥—É—â–µ–º—É –º–µ—Å—è—Ü—É
    if (minDate) {
      const firstDayOfNewMonth = new Date(newDate.getFullYear(), newDate.getMonth(), 1);
      if (firstDayOfNewMonth < minDate) {
        return; // –ù–µ –ø–æ–∑–≤–æ–ª—è–µ–º –ø–µ—Ä–µ—Ö–æ–¥–∏—Ç—å –∫ –º–µ—Å—è—Ü—É —Ä–∞–Ω—å—à–µ minDate
      }
    }
    
    setCurrentDate(newDate);
  };

  const handleNextMonth = () => {
    const newDate = new Date(currentDate.getFullYear(), currentDate.getMonth() + 1);
    
    // –ü—Ä–æ–≤–µ—Ä—è–µ–º, –º–æ–∂–Ω–æ –ª–∏ –ø–µ—Ä–µ–π—Ç–∏ –∫ —Å–ª–µ–¥—É—é—â–µ–º—É –º–µ—Å—è—Ü—É
    if (maxDate) {
      const lastDayOfNewMonth = new Date(newDate.getFullYear(), newDate.getMonth() + 1, 0);
      if (lastDayOfNewMonth > maxDate) {
        return; // –ù–µ –ø–æ–∑–≤–æ–ª—è–µ–º –ø–µ—Ä–µ—Ö–æ–¥–∏—Ç—å –∫ –º–µ—Å—è—Ü—É –ø–æ–∑–∂–µ maxDate
      }
    }
    
    setCurrentDate(newDate);
  };

  const handleToday = () => {
    const today = new Date();
    // –ò—Å–ø–æ–ª—å–∑—É–µ–º –ª–æ–∫–∞–ª—å–Ω–æ–µ –≤—Ä–µ–º—è –¥–ª—è –∏–∑–±–µ–∂–∞–Ω–∏—è –ø—Ä–æ–±–ª–µ–º —Å —á–∞—Å–æ–≤—ã–º–∏ –ø–æ—è—Å–∞–º–∏
    const year = today.getFullYear();
    const month = String(today.getMonth() + 1).padStart(2, '0');
    const dayStr = String(today.getDate()).padStart(2, '0');
    const formattedDate = `${year}-${month}-${dayStr}`;
    
    onChange(formattedDate);
    if (onClose) onClose();
  };

  const renderCalendar = () => {
    const daysInMonth = getDaysInMonth(currentDate);
    const firstDay = getFirstDayOfMonth(currentDate);
    const days = [];

    // –ü—É—Å—Ç—ã–µ —è—á–µ–π–∫–∏ –¥–ª—è –Ω–∞—á–∞–ª–∞ –º–µ—Å—è—Ü–∞
    for (let i = 0; i < firstDay; i++) {
      days.push(<div key={`empty-${i}`} className="calendar-day empty"></div>);
    }

    // –î–Ω–∏ –º–µ—Å—è—Ü–∞
    for (let day = 1; day <= daysInMonth; day++) {
      const isSelected = selectedDate && 
        selectedDate.getDate() === day && 
        selectedDate.getMonth() === currentDate.getMonth() && 
        selectedDate.getFullYear() === currentDate.getFullYear();
      
      const isToday = new Date().toDateString() === new Date(currentDate.getFullYear(), currentDate.getMonth(), day).toDateString();
      const isDisabled = isDateDisabled(day);

      days.push(
        <div
          key={day}
          className={`calendar-day ${isSelected ? 'selected' : ''} ${isToday ? 'today' : ''} ${isDisabled ? 'disabled' : ''}`}
          onClick={() => handleDateSelect(day)}
        >
          {day}
        </div>
      );
    }

    return days;
  };

  const monthNames = [
    '–Ø–Ω–≤–∞—Ä—å', '–§–µ–≤—Ä–∞–ª—å', '–ú–∞—Ä—Ç', '–ê–ø—Ä–µ–ª—å', '–ú–∞–π', '–ò—é–Ω—å',
    '–ò—é–ª—å', '–ê–≤–≥—É—Å—Ç', '–°–µ–Ω—Ç—è–±—Ä—å', '–û–∫—Ç—è–±—Ä—å', '–ù–æ—è–±—Ä—å', '–î–µ–∫–∞–±—Ä—å'
  ];

  const dayNames = ['–ü–Ω', '–í—Ç', '–°—Ä', '–ß—Ç', '–ü—Ç', '–°–±', '–í—Å'];

  return (
    <div className="calendar-container" ref={calendarRef}>
      <div 
        className="calendar-input"
        onClick={() => {
          if (isOpen) {
            if (onClose) onClose();
          } else {
            if (onOpen) onOpen();
          }
        }}
      >
        <span className="calendar-value">
          {formatDate(selectedDate)}
        </span>
        <span className="calendar-icon">üìÖ</span>
      </div>
      
      {isOpen && (
        <div className={`calendar-dropdown ${!position.top ? 'position-top' : ''} ${!position.left ? 'position-right' : 'position-left'}`}>
          <div className="calendar-header">
            <button className="calendar-nav-btn" onClick={handlePrevMonth}>
              ‚Äπ
            </button>
            <div className="calendar-month-year">
              {monthNames[currentDate.getMonth()]} {currentDate.getFullYear()}
            </div>
            <button className="calendar-nav-btn" onClick={handleNextMonth}>
              ‚Ä∫
            </button>
          </div>
          
          <div className="calendar-weekdays">
            {dayNames.map(day => (
              <div key={day} className="calendar-weekday">
                {day}
              </div>
            ))}
          </div>
          
          <div className="calendar-days">
            {renderCalendar()}
          </div>
          
          <div className="calendar-footer">
            <button className="calendar-today-btn" onClick={handleToday}>
              –°–µ–≥–æ–¥–Ω—è
            </button>
            <button className="calendar-clear-btn" onClick={() => {
              onChange('');
              if (onClose) onClose();
            }}>
              –û—á–∏—Å—Ç–∏—Ç—å
            </button>
          </div>
        </div>
      )}
    </div>
  );
};
